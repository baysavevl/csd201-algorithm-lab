     public boolean checkData()
    {
        
        String regex = "\\d+"; 
        
        if (txtDec.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Number must not be empty");
            return false;
        }
        
        if (!txtDec.getText().matches(regex))
        {
            JOptionPane.showMessageDialog(this, "Please Enter Number");
            return false;
        }        
        
        return true;
        
    }
    
    
    


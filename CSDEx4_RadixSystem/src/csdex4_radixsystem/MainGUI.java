    private void btnConvertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertActionPerformed
        // TODO add your handling code here:
        RadixSystem method = new RadixSystem();
        
        if (checkData())
        {
            txtBin.setText(method.covertBase2(Long.parseLong(txtDec.getText())));
            txtOct.setText(method.covertBase8(Long.parseLong(txtDec.getText())));
            txtHex.setText(method.covertBase16(Long.parseLong(txtDec.getText())));
            
            txtDec.setEditable(false);
            txtBin.setEditable(false);
            txtOct.setEditable(false);
            txtHex.setEditable(false);
        }
        
    }//GEN-LAST:event_btnConvertActionPerformed

